function onCastSpell(cid, var)
	doCreatureSay(cid, "SURF!", TALKTYPE_MONSTER)
	local msg = getCreatureMaster(cid)
	if isPlayer(msg) == true then
	local mon = getCreatureName(cid)
	doCreatureSay(msg, ""..mon..", use Surf!", TALKTYPE_SAY)
	end
local parameters = { cid = cid, var = var}
if getCreatureLookDir(cid) == 0 then
addEvent(onCastSpell1, 000, parameters)
addEvent(onCastSpell2, 440, parameters)
addEvent(onCastSpell3, 820, parameters)
addEvent(onCastSpell33, 1200, parameters)
addEvent(onCastSpell333, 1600, parameters)
elseif getCreatureLookDir(cid) == 1 then
addEvent(onCastSpell4, 000, parameters)
addEvent(onCastSpell5, 440, parameters)
addEvent(onCastSpell6, 820, parameters)
addEvent(onCastSpell66, 1200, parameters)
addEvent(onCastSpell666, 1600, parameters)
elseif getCreatureLookDir(cid) == 2 then
addEvent(onCastSpell7, 000, parameters)
addEvent(onCastSpell8, 440, parameters)
addEvent(onCastSpell9, 820, parameters)
addEvent(onCastSpell99, 1200, parameters)
addEvent(onCastSpell999, 1600, parameters)
elseif getCreatureLookDir(cid) == 3 then
addEvent(onCastSpell10, 000, parameters)
addEvent(onCastSpell11, 440, parameters)
addEvent(onCastSpell12, 820, parameters)
addEvent(onCastSpell122, 1200, parameters)
addEvent(onCastSpell1222, 1600, parameters)
end
return TRUE
end